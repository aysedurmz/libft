#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*s1;
	size_t	len;

	i = 0;
	len = ft_strlen(s);
	s1 = (char *)malloc(len + 1);
	if (!s1)
		return (NULL);
	while (s[i])
	{
		s1[i] = s[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
